export class Candidat {
    id: number =0;
    nom: string ="";
    email: string="";
    password: string="";
    country: string="";
    


public Candidat (id:number,nom: string ,email: string,password : string,country : string){
    this.id=id;
    this.nom=nom;
    this.email=email;
    this.password=password;
    this.country=country;
    
}











}
